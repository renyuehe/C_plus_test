//大端：高位字节排放在内存的低地址端，低位字节排放在内存的高地址端。（CPU对操作数的存放方式是从高字节到低字节）
//小端：低位字节排放在内存的低地址端，高位字节排放在内存的高地址端。（CPU对操作数的存放方式是从低字节到高字节）
 
#include<stdio.h>
int main()
{
	
	//小端模式：低地址存低字节
	//大端模式：低地址存高字节
	int temp = 0x1234;//x=0x1234,占两个字节,0x12是高字节,0x34在低字节
	char *p = (char *)&temp;
	 
	printf("p[0] = %#x, p[1] = %#x\n",p[0], p[1]);
	printf("*p = %#x\n",*p);
	 
	if(*p == 0x34)
	printf("小端模式\n");
	else if(*p == 0x12)
	printf("大端模式\n");
 
return 0;

}
